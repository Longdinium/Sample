
public class IfElseSample {

    public static void main(String[] args) {
        int score = -10; // 成績値

        // scoreが60以上ならば
        if (score >= 60) {
            // 合格です！と表示
            System.out.println("合格です！");
        } else {
            // 再テストですと表示
            System.out.println("再テストです");

        }

    }

}
